function L=compute_L6_12(K)

L=zeros(6,12);

%% Extract vectors
v1=K(:,1);
v2=K(:,2);
v3=K(:,3);

%% Compute differences
% First eigenvector values
dx112 = v1(1) - v1(4);  dy112 = v1(2) - v1(5);  dz112 = v1(3) - v1(6);  % x1-x2 ; y1-y2 ; z1-z2
dx113 = v1(1) - v1(7);  dy113 = v1(2) - v1(8);  dz113 = v1(3) - v1(9);  % x1-x3 ; y1-y3 ; z1-z3
dx114 = v1(1) - v1(10); dy114 = v1(2) - v1(11); dz114 = v1(3) - v1(12); % x1-x4 ; y1-y4 ; z1-z4
dx123 = v1(4) - v1(7);  dy123 = v1(5) - v1(8);  dz123 = v1(6) - v1(9);  % x2-x3 ; y2-y3 ; z2-z3
dx124 = v1(4) - v1(10); dy124 = v1(5) - v1(11); dz124 = v1(6) - v1(12); % x2-x4 ; y2-y4 ; z2-z4
dx134 = v1(7) - v1(10); dy134 = v1(8) - v1(11); dz134 = v1(9) - v1(12); % x3-x4 ; y3-y4 ; z3-z4

% Second eigenvector values
dx212 = v2(1) - v2(4);  dy212 = v2(2) - v2(5);  dz212 = v2(3) - v2(6);  % x1-x2 ; y1-y2 ; z1-z2
dx213 = v2(1) - v2(7);  dy213 = v2(2) - v2(8);  dz213 = v2(3) - v2(9);  % x1-x3 ; y1-y3 ; z1-z3
dx214 = v2(1) - v2(10); dy214 = v2(2) - v2(11); dz214 = v2(3) - v2(12); % x1-x4 ; y1-y4 ; z1-z4
dx223 = v2(4) - v2(7);  dy223 = v2(5) - v2(8);  dz223 = v2(6) - v2(9);  % x2-x3 ; y2-y3 ; z2-z3
dx224 = v2(4) - v2(10); dy224 = v2(5) - v2(11); dz224 = v2(6) - v2(12); % x2-x4 ; y2-y4 ; z2-z4
dx234 = v2(7) - v2(10); dy234 = v2(8) - v2(11); dz234 = v2(9) - v2(12); % x3-x4 ; y3-y4 ; z3-z4

% Third eigenvector values
dx312 = v3(1) - v3(4);  dy312 = v3(2) - v3(5);  dz312 = v3(3) - v3(6);  % x1-x2 ; y1-y2 ; z1-z2
dx313 = v3(1) - v3(7);  dy313 = v3(2) - v3(8);  dz313 = v3(3) - v3(9);  % x1-x3 ; y1-y3 ; z1-z3
dx314 = v3(1) - v3(10); dy314 = v3(2) - v3(11); dz314 = v3(3) - v3(12); % x1-x4 ; y1-y4 ; z1-z4
dx323 = v3(4) - v3(7);  dy323 = v3(5) - v3(8);  dz323 = v3(6) - v3(9);  % x2-x3 ; y2-y3 ; z2-z3
dx324 = v3(4) - v3(10); dy324 = v3(5) - v3(11); dz324 = v3(6) - v3(12); % x2-x4 ; y2-y4 ; z2-z4
dx334 = v3(7) - v3(10); dy334 = v3(8) - v3(11); dz334 = v3(9) - v3(12); % x3-x4 ; y3-y4 ; z3-z4


%% L Coefficient matrix calculation
% R[1]
L(1,1) =        dx112 * dx112 + dy112 * dy112;      %b1*b1
L(1,2) = 2.0 *  (dx112 * dx212 + dy112 * dy212);    %b1*b2
L(1,4) = 2.0 *  (dx112 * dx312 + dy112 * dy312);    %b1*b3
L(1,3) =        dx212 * dx212 + dy212 * dy212;      %b2*b2
L(1,5) = 2.0 *  (dx212 * dx312 + dy212 * dy312);    %b2*b3
L(1,6) =        dx312 * dx312 + dy312 * dy312;      %b3*b3
L(1,7) =        dz112 * dz112;                  %f²*b1*b1
L(1,8) = 2.0 *  (dz112 * dz212);                %f²*b1*b2
L(1,9) = 2.0 *  (dz112 * dz312);                %f²*b1*b3
L(1,10) =       dz212 * dz212;                  %f²*b2*b2
L(1,11) = 2.0 * (dz212 * dz312);                %f²*b2*b3
L(1,12) =       dz312 * dz312;                  %f²*b3*b3

% R[2]
L(2,1) =        dx113 * dx113 + dy113 * dy113;      %b1*b1
L(2,2) = 2.0 *  (dx113 * dx213 + dy113 * dy213);    %b1*b2
L(2,4) = 2.0 *  (dx113 * dx313 + dy113 * dy313);    %b1*b3
L(2,3) =        dx213 * dx213 + dy213 * dy213;      %b2*b2
L(2,5) = 2.0 *  (dx213 * dx313 + dy213 * dy313);    %b2*b3
L(2,6) =        dx313 * dx313 + dy313 * dy313;      %b3*b3
L(2,7) =        dz113 * dz113;                  %f²*b1*b1
L(2,8) = 2.0 *  (dz113 * dz213);                %f²*b1*b2
L(2,9) = 2.0 *  (dz113 * dz313);                %f²*b1*b3
L(2,10) =       dz213 * dz213;                  %f²*b2*b2
L(2,11) = 2.0 * (dz213 * dz313);                %f²*b2*b3
L(2,12) =       dz313 * dz313;                  %f²*b3*b3

% R[3]
L(3,1) =        dx114 * dx114 + dy114 * dy114;      %b1*b1
L(3,2) = 2.0 *  (dx114 * dx214 + dy114 * dy214);    %b1*b2
L(3,4) = 2.0 *  (dx114 * dx314 + dy114 * dy314);    %b1*b3
L(3,3) =        dx214 * dx214 + dy214 * dy214;      %b2*b2
L(3,5) = 2.0 *  (dx214 * dx314 + dy214 * dy314);    %b2*b3
L(3,6) =        dx314 * dx314 + dy314 * dy314;      %b3*b3
L(3,7) =        dz114 * dz114;                  %f²*b1*b1
L(3,8) = 2.0 *  (dz114 * dz214);                %f²*b1*b2
L(3,9) = 2.0 *  (dz114 * dz314);                %f²*b1*b3
L(3,10) =       dz214 * dz214;                  %f²*b2*b2
L(3,11) = 2.0 * (dz214 * dz314);                %f²*b2*b3
L(3,12) =       dz314 * dz314;                  %f²*b3*b3

% R[4]
L(4,1) =        dx123 * dx123 + dy123 * dy123;      %b1*b1
L(4,2) = 2.0 *  (dx123 * dx223 + dy123 * dy223);    %b1*b2
L(4,4) = 2.0 *  (dx123 * dx323 + dy123 * dy323);    %b1*b3
L(4,3) =        dx223 * dx223 + dy223 * dy223;      %b2*b2
L(4,5) = 2.0 *  (dx223 * dx323 + dy223 * dy323);    %b2*b3
L(4,6) =        dx323 * dx323 + dy323 * dy323;      %b3*b3
L(4,7) =        dz123 * dz123;                  %f²*b1*b1
L(4,8) = 2.0 *  (dz123 * dz223);                %f²*b1*b2
L(4,9) = 2.0 *  (dz123 * dz323);                %f²*b1*b3
L(4,10) =       dz223 * dz223;                  %f²*b2*b2
L(4,11) = 2.0 * (dz223 * dz323);                %f²*b2*b3
L(4,12) =       dz323 * dz323;                  %f²*b3*b3

% R[5]
L(5,1) =        dx124 * dx124 + dy124 * dy124;      %b1*b1
L(5,2) = 2.0 *  (dx124 * dx224 + dy124 * dy224);    %b1*b2
L(5,4) = 2.0 *  (dx124 * dx324 + dy124 * dy324);    %b1*b3
L(5,3) =        dx224 * dx224 + dy224 * dy224;      %b2*b2
L(5,5) = 2.0 *  (dx224 * dx324 + dy224 * dy324);    %b2*b3
L(5,6) =        dx324 * dx324 + dy324 * dy324;      %b3*b3
L(5,7) =        dz124 * dz124;                  %f²*b1*b1
L(5,8) = 2.0 *  (dz124 * dz224);                %f²*b1*b2
L(5,9) = 2.0 *  (dz124 * dz324);                %f²*b1*b3
L(5,10) =       dz224 * dz224;                  %f²*b2*b2
L(5,11) = 2.0 * (dz224 * dz324);                %f²*b2*b3
L(5,12) =       dz324 * dz324;                  %f²*b3*b3

% R[6]
L(6,1) =        dx134 * dx134 + dy134 * dy134;      %b1*b1
L(6,2) = 2.0 *  (dx134 * dx234 + dy134 * dy234);    %b1*b2
L(6,4) = 2.0 *  (dx134 * dx334 + dy134 * dy334);    %b1*b3
L(6,3) =        dx234 * dx234 + dy234 * dy234;      %b2*b2
L(6,5) = 2.0 *  (dx234 * dx334 + dy234 * dy334);    %b2*b3
L(6,6) =        dx334 * dx334 + dy334 * dy334;      %b3*b3
L(6,7) =        dz134 * dz134;                  %f²*b1*b1
L(6,8) = 2.0 *  (dz134 * dz234);                %f²*b1*b2
L(6,9) = 2.0 *  (dz134 * dz334);                %f²*b1*b3
L(6,10) =       dz234 * dz234;                  %f²*b2*b2
L(6,11) = 2.0 * (dz234 * dz334);                %f²*b2*b3
L(6,12) =       dz334 * dz334;                  %f²*b3*b3


%% Finished
